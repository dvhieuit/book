function change(e,name){
  if(name==="s-t") e.style.color = "#1AB2E8"
    else if(name==="s-g") e.style.color = "#EA4335"
      else if(name==="s-f") e.style.color = "#4267B2"
        else if(name==="s-y") e.style.color = "red";
          else if(name==="s-i") e.style.color = "B33485";
        else e.style.color = "green"
}
function reset(e){
  e.style.color = "black";
}